public class ConvertToAscii {
    public static void main(String[] args) {

        int a = (int) 'a';
        int A = (int) 'A';

        int z = (int) 'z';
        int Z = (int) 'Z';

        System.out.println("a: " + a);
        System.out.println("A: " + A);
        System.out.println("z: " + z);
        System.out.println("Z: " + Z);

        int charInt;
        charInt = 'A';
        System.out.println(charInt);

        int length = 'z' - 'a' + 1;
        System.out.println(length);

    }
}